#include "../../includes/minishell.h"

int	ft_cmdcmp(char *s1, char **s2)
{
	int	i;

	i = -1;
	while (s2[++i])
	{
		if (ft_strcmp(s1, s2[i]) == 0)
			return (1);
	}
	return (0);
}
